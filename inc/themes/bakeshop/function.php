Hooks::attach('post_sqladd_action', function($args) {
    if (($_GET['type'] ?? '') !== 'nixomers') return;
    $vars = $args[0] ?? [];
    $id = $args[1] ?? 0;

    $params = [
        'info_ingredients', 'info_allergens', 
        'nutri_calories', 'nutri_sugar', 
        'nutri_fat', 'nutri_protein'
    ];

    foreach ($params as $p) {
        if (isset($_POST[$p])) {
            if (Posts::existParam($p, $id)) {
                Posts::editParam($p, $_POST[$p], $id);
            } else {
                Posts::addParam($p, $_POST[$p], $id);
            }
        }
    }
});
