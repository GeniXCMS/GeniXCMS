    public function __construct()
    {
        Hooks::attach('init', function() {
            AdminMenu::addChild('themes', [
                'label' => _('Bakeshop Options'),
                'icon' => 'bi bi-palette2',
                'url' => 'index.php?page=themes&view=options',
                'access' => 0
            ]);
        });
    }

    public static function options()
    {
        $opt_raw = Options::get('bakeshop_options');
        $o_db = json_decode($opt_raw, true);
        $defaults = self::getDefaults();
        return array_merge($defaults, (is_array($o_db) ? $o_db : []));
    }

    public static function checkDB()
    {
        return Options::validate('bakeshop_options');
    }
